package com.example.pralhad.dailyexpneses.pojoClass;

public class user {
    private int userId;
    private int userFName;
    private int userLName;
    private int userContact;
    private int userPassword;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getUserFName() {
        return userFName;
    }

    public void setUserFName(int userFName) {
        this.userFName = userFName;
    }

    public int getUserLName() {
        return userLName;
    }

    public void setUserLName(int userLName) {
        this.userLName = userLName;
    }

    public int getUserContact() {
        return userContact;
    }

    public void setUserContact(int userContact) {
        this.userContact = userContact;
    }

    public int getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(int userPassword) {
        this.userPassword = userPassword;
    }
}
